#include "folder.hpp"
// ----------------------------------------------------------------------------
namespace folder
{
   // -------------------------------------------------------------------------

   // -------------------------------------------------------------------------
   namespace s    = std          ;
   namespace st   = s::tr1       ;
   // -------------------------------------------------------------------------

   // -------------------------------------------------------------------------
   folder::initializer::initializer (
      __int64 const     sz,
      __int64 const     fc,
      tstring const &   n,
      s::size_t const   sfz
      )
      :  size (sz)
      ,  file_count (fc)
      ,  name (n)
      ,  sub_folders_size (sfz)
   {
   }
   // -------------------------------------------------------------------------

   // -------------------------------------------------------------------------
   folder::folder ()
      :  size        (0)
      ,  file_count  (0)
   {
   }
   // -------------------------------------------------------------------------

   // -------------------------------------------------------------------------
   folder::folder (
      initializer const & init)
      :  size        (init.size)
      ,  file_count  (init.file_count)
      ,  name        (init.name)
      ,  sub_folders (init.sub_folders_size, &folder::empty)
   {
   }
   // -------------------------------------------------------------------------

   // -------------------------------------------------------------------------
   folder const folder::empty;
   // -------------------------------------------------------------------------

   // -------------------------------------------------------------------------
}
